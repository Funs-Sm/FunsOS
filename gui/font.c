void font_draw_char(gfx_context_t *ctx, char c, int32_t x, int32_t y, gfx_color_t fg, gfx_color_t bg) {
    if (c < 32 || c > 127) c = 32;
    const uint8_t *glyph = font_data[(uint8_t)c - 32];
    for (int row = 0; row < FONT_GLYPH_HEIGHT; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_GLYPH_WIDTH; col++) {
            if (bits & (0x80 >> col)) {
                gfx_set_pixel(ctx, x + col, y + row, fg);
            } else if (bg != 0xFFFFFFFF) {
                gfx_set_pixel(ctx, x + col, y + row, bg);
            }
        }
    }
}

void font_draw_string(gfx_context_t *ctx, const char *str, int32_t x, int32_t y, gfx_color_t fg, gfx_color_t bg) {
    int32_t cx = x;
    while (*str) {
        font_draw_char(ctx, *str, cx, y, fg, bg);
        cx += FONT_GLYPH_WIDTH;
        str++;
    }
}

void font_measure_string(const char *str, uint32_t *width, uint32_t *height) {
    uint32_t len = 0;
    while (str[len]) len++;
    *width = len * FONT_GLYPH_WIDTH;
    *height = FONT_GLYPH_HEIGHT;
}
